public class Latihan {
    public static void main(String[] args) {
        
        int i = 1;
        System.out.println("i\t: " + i);
        System.out.println("++i\t: " + ++i);
        System.out.println("i++\t: " + i++);

        int a = 2;
        System.out.println("--a\t: " + --a);
        System.out.println("a--\t: " + a--);
        System.out.println("a \t: " + a);
    }  
}
